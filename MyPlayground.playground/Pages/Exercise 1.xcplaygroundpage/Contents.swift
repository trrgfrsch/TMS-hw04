import UIKit

//функция для сложения чисел

func sum(number1: Int, number2: Int) -> Int {
    return number1 + number2
}

sum(number1: 3, number2: 6543)

//функция для умножения чисел

func multiplication(number1: Int, number2: Int) -> Int {
    return number1 * number2
}

multiplication(number1: 3, number2: 2)

//функция для деления чисел

func division(number1: Int, number2: Int) -> Int {
    return number1 / number2
}

division(number1: 4, number2: 2)

// функция для вычитания чисел

func difference(number1: Int, number2: Int) -> Int {
    return number1 - number2
}

difference(number1: 54, number2: 2)

// функция для нахождения остатка от деления

func remainder(number1: Int, number2: Int) -> Int {
    return number1 % number2
}

remainder(number1: 13, number2: 2)


